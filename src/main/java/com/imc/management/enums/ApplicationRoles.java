package com.imc.management.enums;



/**
 * The ApplicationRoles enum represents the different roles available in the application.
 * It defines the roles ADMIN, USER, EDITOR, and VIEWER.
 */

public enum ApplicationRoles {

	/**
	 * The role for an administrator.
	 */
	ADMIN, 
	
	/**
	 * The role for a regular user.
	 */
	USER, 
	
	/**
	 * The role for an editor.
	 */
	
	EDITOR, 
	
	/**
	 * The role for a viewer.
	 */
	
	VIEWER

}
